//FORM 1

$("#form1").hide();


$( "#plus1" ).click(function() {		
  $("#box1").hide();
  $("#form1").show();
});

$("#submit1").click(function(){
	$("#form1").hide();
	$("#project1").hide();
	$("#box1").show();
	$( "#nameEntry1" ).append($('#name1').val().trim());
	$( "#urlEntry1" ).append($('#url1').val().trim());
	$( "#descrEntry1" ).append($('#descr1').val().trim());
	return false;
});

